#include "../pipex.h"

int ft_strncmp(char *s1, char *s2, int n)
{
    int i;
    
    i = 0;
    if (n == 0)
        return (0);
    while (s1[i] && s2[i] && i < n)
    {   
            if (s2[i] != s1[i])
                return (-1);
            i++;
    }
   // printf("QWEQWEQW %c et %c \n", s1[i], s2[i]);
    return (0);
}